<h1>What's Shenmue ?</h1>

<p>Shenmue is a 1999 adventure game developed by SEGA-AM2 and published by Sega for the Sega Dreamcast, produced and directed by Yu Suzuki.
Suzuki coined a genre title, "FREE" (Full Reactive Eyes Entertainment), for the game, based on the interactivity and freedom he wanted to give to the player.</p>

<p>Suzuki intended to achieve this by simulating aspects of real life through the game, such as the day/night/sleep system, real time variable weather effects 
(unheard of in a game of this kind at the time) and various other interactive elements such as vending machines, arcades, and convenience stores.</p>

<p>Shenmue borrows gameplay from many different genres but largely consists of open-world adventure segments with real-time 3D battles interspersed throughout.
Shenmue was followed by a sequel, Shenmue II, with at least one more planned as a Dreamcast killer app series, although production on the series ended 
with the second game, leaving the story at an unresolved cliffhanger.</p> 

