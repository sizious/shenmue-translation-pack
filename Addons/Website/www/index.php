<h1>Project Information</h1>

<p>It all started on the French <a target="_blank" href="http://www.metagames-eu.com/forums/dreamcast/projet-traduction-des-sous-titres-de-shenmue-1-et-2-a-65066.html">Metagames</a> 
forum. A lot of people were asking if Shenmue 1 or 2 were available in French language. 
The answer was always the same: None of Shenmue episode was translated in another language. All in-game subtitles are in English.</p>

<p>One day of reading yet another post about Shenmue, <strong>Manic</strong> decided to check if that was possible to modify the in-game subtitles. It worked fine. That was in 2006 and we're still going on!</p>

<p>The tools to modify cinematic subtitles for Shenmue 2 were released back in 2007. The NPC (Non-Playing Characters) subtitles tool was released in 2008.</p>

<p>Later, the tools were adapted to hack Shenmue 1 and What's Shenmue game files. <a target="_blank" href="http://sbibuilder.shorturl.com/">SiZiOUS</a> joined the team around 2008. 
He's the main coder of this project now.</p>

<p>The <strong>SHENTRAD Team</strong>, here to breake the language barrier!</p>

